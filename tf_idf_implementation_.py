def compute_tf(docs):
    tf_list = []

    for doc in docs:
      word_count = Counter(doc)
      total_words = len(doc)

      tf = {}
      for word, count in word_count.items():
        tf[word] = count / total_words


      tf_list.append(tf)

    return tf_list


