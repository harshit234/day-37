def compute_idf(docs, vocab):
    N = len(docs)
    doc_freq = defaultdict(int)

    for doc in docs:
        unique_words = set(doc)
        for word in unique_words:
            doc_freq[word] += 1

    idf = {}
    for word in vocab:
        df = doc_freq[word]
        idf[word] = math.log(N / (1 + df))

    return idf

idf = compute_idf(docs, vocab)

def compute_tfidf(tf_list, idf, vocab):
    tfidf_matrix = []

    for tf in tf_list:
        row = {}
        for word, tf_val in tf.items():
            row[vocab[word]] = tf_val * idf[word]
        tfidf_matrix.append(row)

    return tfidf_matrix

tfidf_matrix = compute_tfidf(tf_list, idf, vocab)

